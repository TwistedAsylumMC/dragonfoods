// All returns all the food items that need to be registered.
func All() []world.Item {
	return all
}

// consumeDuration returns the duration of the food's consumption from the provided number of ticks.
func consumeDuration(ticks int) time.Duration {
	return time.Duration(ticks*50) * time.Millisecond
}

// textureFromName attempts to load a texture from the assets directory using the provided item name.
func textureFromName(name string) image.Image {
	texture, err := os.OpenFile(fmt.Sprintf("assets/%s.png", name), os.O_RDONLY, os.ModePerm)
	if err != nil {
		panic(err)
	}
	defer texture.Close()
	img, err := png.Decode(texture)
	if err != nil {
		panic(err)
	}
	return img
}
